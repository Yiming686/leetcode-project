package LeetCode.JavaList;

public class ListNode {
	public int val;
	public ListNode next;
	public ListNode(int value, ListNode next) {
		super();
		this.val = value;
		this.next = next;
	}
	public ListNode() {
		super();
	}
	public ListNode(int value) {
		super();
		this.val = value;
		this.next = null;
	}
	
	
}
